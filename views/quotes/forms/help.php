            <h2>OWNERS</h2>
            Building(s) and Business Property.
            <table class="table table-striped table-bordered">
                <tr>
                    <td><strong>APARTMENT</strong></td>
                    <td>
                        Building(s)-<br />
                        1) Not over 6 stories in height.<br />
                        2) Containing 5 to 60 units.<br />
                        3) Does not have mercantile occupancy.
                    </td>
                </tr>
                <tr>
                    <td><strong>HOTEL / MOTEL</strong></td>
                    <td>
                        Building(s)-<br />
                        1) Not over 3 stories in height.<br />
                        2) Containing 5 to 60 units.<br />
                        3) Mercantile occupancy not to exceed 250 square feet.<br />
                        For pool liability, See Optional Liability Screen for additional charge.
                    </td>
                </tr>
                <tr>
                    <td><strong>OFFICE</strong></td>
                    <td>
                        Building(s)-<br />
                        1) Primarily used for office purposes.<br />
                        2) Not over 4 stories in height.<br />
                        3) Does not exceed 15,000 sq. ft. per floor.<br />
                        4) Does not have mercantile occupancy.
                    </td>
                </tr>
                <tr>
                    <td><strong>SERVICE</strong></td>
                    <td>
                        Building(s)-<br />
                        1) Primarily used for service businesses shown in the classification list.<br />
                        2) Not over 4 stories in height.<br />
                        3) Does not exceed 15,000 sq. ft. per floor.
                    </td>
                </tr>
                <tr>
                    <td><strong>MERCANTILE</strong></td>
                    <td>
                        Building(s)-<br />
                        1) Principal occupancy is buying and selling of merchandise.<br />
                        2) Not over 4 stories in height.<br />
                        3) Does not exceed 15,000 sq. ft. per floor.<br />
                    </td>
                </tr>
                <tr>
                    <td><strong>CHURCH</strong></td>
                    <td>
                        Building(s)-<br />
                        1) Principal occupancy is for religious purposes.<br />
                        2) Does not exceed 15,000 sq. ft. per floor.<br />
                    </td>
                </tr>
            </table>
