                    <td><strong>Frame</strong></td>
                    <td>
                        Exterior walls are wood or other combustible materials,
                        including construction where combustible materials
                        are combined with other materials (such as brick veneer,
                        stone veneer, wood iron-clad, stucco on wood).
                        One third of exterior walls are of frame or combustible
                        materials.
                    </td>
                </tr>
                <tr>
                    <td><strong>Masonry</strong></td>
                    <td>
                        Exterior walls are constructed of masonry material.
                        Examples: adobe, brick, concrete, gypsum block,
                        hollow concrete block, stone, tile or similar materials,
                        and where the floors and roof are combustible (disregarding
                        floors resting directly on the ground). Less than one third
                        of exterior walls are of frame or combustible construction.
                    </td>
                </tr>
                <tr>
                    <td><strong>Fire</strong></td>
                    <td>
                        Resistive	Exterior walls and the floors and roof are constructed of
                        masonry or fire resistive materials having a fire resistance
                        rating of not less than two hours. All exterior walls,
                        ceilings and roofs are of non-combustible construction.
                    </td>
                </tr>
                <tr>
                    <td><strong>Note:</strong></td>
                    <td>
                        Metal buildings with metal or frame supports shall be classified
                        as frame.
                    </td>
                </tr>
            </table>
